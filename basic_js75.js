function caseInSwitch(val) {
    let answer = "";
    // Only change code below this line
    switch (val){
        case 1:
            val = "alpha";
            break;
        case 2:
            val = "beta";
            break;
        case 3:
            val = "gamma";
            break;
        case 4:
            val = "delta";
            break
    }
    answer = val;
    // Only change code above this line
    return answer;
}

